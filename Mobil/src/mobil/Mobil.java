package mobil;
public class Mobil {
        int noPlat;
        String warna;
        String manufaktur;
        int tahun;
        void Mobil() {
        }
        public void setNoPlat(int no) {
        noPlat = no;
        }
        public int getNoPlat() {
        return noPlat;
        }
       public void setWarna(String w) {
        warna = w;
        }
        public String getWarna() {
        return warna;
        }
        public void setManufaktur(String m) {
        manufaktur = m;
        }
        public String getManufaktur() {
        return manufaktur;
        }
        public void setTahun(int t) {
        tahun = t;
        }
        public int getTahun() {
        return tahun;
    }
    
}
